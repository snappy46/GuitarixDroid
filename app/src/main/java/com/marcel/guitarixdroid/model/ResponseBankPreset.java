package com.marcel.guitarixdroid.model;

/**
 * Wrapper class to handle JSON response from Guitarix server.
 */
public class ResponseBankPreset {

    private String jsonrpc;
    private String id;
    private ResultBankPreset result;

    public String getJsonrpc() {
        return jsonrpc;
    }

    public void setJsonrpc(String jsonrpc) {
        this.jsonrpc = jsonrpc;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public ResultBankPreset getResult() {
        return result;
    }

    public void setResult(ResultBankPreset result) {
        this.result = result;
    }

}